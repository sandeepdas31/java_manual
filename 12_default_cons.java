//purpose of default constructor
	class defaultcons{  
	int id;
	String name;  
	  
	void display(){System.out.println(id+" "+name);}  
	  
	public static void main(String args[]){  
		defaultcons s1=new defaultcons();  
		defaultcons s2=new defaultcons(); 
	
	s1.display();  
	s2.display();  
	}  
	}  
